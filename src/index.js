const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

const NUM_MORSE_TABLE = {
  '**********': ' '
};

for (i in MORSE_TABLE) {
  name = i.replace(/-/g, '11').replace(/\./g, '10').padStart(10, '00');
  NUM_MORSE_TABLE[name] = MORSE_TABLE[i];
}

function decode(expr) {
  return expr.match(/.{10}/g).map(item => NUM_MORSE_TABLE[item]).join('');
}

module.exports = {
    decode
}
